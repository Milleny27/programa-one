console.log("Conversão de Tipos");

console.log("ano" + 2020); // concatena

console.log("2" + "2"); // concatena

console.log(parseInt("2") + parseInt("2")); // Convertendo String para inteiro, conversão explícita
// parseFloat -> converte para o tipo ponto flutuante

console.log("10" / "2"); // Faz a conversão implícita

console.log("Ricardo" / "2"); // Retorna NaN

console.log(6.5); // ponto flutuante

console.log(6,5); // A vírgula é lida como espaço